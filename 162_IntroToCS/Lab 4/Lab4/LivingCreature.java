
/**
 * The living creatures are included in this interface
 * 
 * @author Tiffany Jansen 
 * @version 04.22.2017
 */
public interface LivingCreature
{
       
    /**
     * Adds a year to the age of the animal.
     */
    public int incrementAge();
    
    /**
     * Is the animal alive?
     * @return isAlive.
     */
    public boolean isAlive();
}
